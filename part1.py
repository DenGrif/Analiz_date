import pandas as pd
import numpy as np

# 1. Самостоятельно создадим DataFrame с данными
data = {
    'Имя': ['Иван', 'Петр', 'Анна', 'Ольга', 'Алексей', 'Марина', 'Сергей', 'Елена', 'Дмитрий', 'Виктория'],
    'Русский язык': [4, 5, 3, 4, 5, 4, 3, 5, 4, 5],
    'Литература': [5, 4, 4, 5, 3, 4, 5, 4, 3, 5],
    'Математика': [3, 4, 5, 4, 5, 3, 4, 5, 4, 3],
    'Физика': [4, 5, 3, 4, 5, 4, 3, 5, 4, 5],
    'Информатика': [5, 4, 4, 5, 3, 4, 5, 4, 3, 5]
}

df = pd.DataFrame(data)

# 2. Выведем первые несколько строк DataFrame, чтобы убедиться, что данные загружены правильно
print("Первые строки DataFrame:")
print(df.head())

# 3. Вычислим среднюю оценку по каждому предмету
mean_scores = df.mean(numeric_only=True)
print("\nСредние оценки по предметам:")
print(mean_scores)

# 4. Вычислим медианную оценку по каждому предмету
median_scores = df.median(numeric_only=True)
print("\nМедианные оценки по предметам:")
print(median_scores)

# 5. Вычислим Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math
print(f"\nQ1 для математики: {Q1_math}")
print(f"Q3 для математики: {Q3_math}")
print(f"IQR для математики: {IQR_math}")

# 6. Вычислим стандартное отклонение
std_dev = df.drop(columns=['Имя']).std()
print("\nСтандартное отклонение по предметам:")
print(std_dev)
